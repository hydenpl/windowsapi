    var ctx = canvas.getContext('2d');
    ctx.save();
    ctx.translate(0, (totalHeight - size) / 2);
    ctx.scale(size / 100, size / 100);
    ctx.beginPath();
    ctx.moveTo(0, 0);
    ctx.lineTo(53.403, 0);
    ctx.lineTo(53.403, 100);
    ctx.lineTo(0, 100);
    ctx.closePath();
    ctx.clip();
    ctx.translate(0, 0);
    ctx.translate(0, 0);
    ctx.scale(1, 1);
    ctx.translate(0, 0);
