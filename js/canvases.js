    ctx.miterLimit = 4;
    ctx.save();
    ctx.fillStyle = "#ffffff";
    ctx.beginPath();
    ctx.moveTo(0, 50);
    ctx.lineTo(14.433, 25);
    ctx.lineTo(28.867, 0);
    ctx.lineTo(53.403, 0);
    ctx.lineTo(38.97, 25);
    ctx.lineTo(24.537, 50);
